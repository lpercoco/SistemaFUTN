package futn;

public class RingedInterval {
	private int ringedNumber;
	private int beginPage;
	private int endPage;
	
	public int getBeginPage() {
		return beginPage;
	}
	public void setBeginPage(int beginPage) {
		this.beginPage = beginPage;
	}
	public int getEndPage() {
		return endPage;
	}
	public void setEndPage(int endPage) {
		this.endPage = endPage;
	}
	public int getRingedNumber() {
		return ringedNumber;
	}
	public void setRingedNumber(int ringedNumber) {
		this.ringedNumber = ringedNumber;
	}

}
